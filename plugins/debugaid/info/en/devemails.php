
<p>If you enter something in "domain" this will change all emails in the DB to be of the form [prefix]-[number]@[domain]</p>

<p>If you only add something (a number) in "add new emails" and a string in "prefix" it will generate the number of emails randomly with mailinator domains using the prefix.</p>
<p>This is to ensure that the test data is not related to any real users</p>
